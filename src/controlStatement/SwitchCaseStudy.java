package controlStatement;

public class SwitchCaseStudy {

	public static void main(String[] args)
	{
		//you are engg student
		//1--1st yr engg
		//2--2nd yr engg
		//3--3rd yr engg
		//4--4th yr engg
		int year=3;
		switch(year)
		{
		case 1:System.out.println("1st yr engg");
		break;
		case 2:System.out.println("2nd yr engg");
		break;
		case 3:System.out.println("3rd yr engg");
		break;
		case 4:System.out.println("4th yr engg");
		break;
		default :System.out.println("invalid year");
		}
		

	}

}
