package controlStatement;

public class IfStudy1 {

	public static void main(String[] args)
	{
		char grade='d';
		if(grade=='A')
		{
			System.out.println("marks range is 75-90");
		}
		else if (grade=='B')
		{
			System.out.println("marks range is 66-75");
			
		}
		else if (grade=='c')
		{
			System.out.println("mark range is 40-66");
			
		}
		else if (grade=='d')
		{
			System.out.println("mark range is <40");
			
		}
	

	}

}
