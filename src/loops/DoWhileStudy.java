package loops;

public class DoWhileStudy {

	public static void main(String[] args)
	{
		int i=1;
		do
		{
			System.out.println(i);
			i++;
		}
		while(i<=20);
		System.out.println("================================================================");
		
		int x=100;
		do
		{
			System.out.println(x);
			x=x-5;
		}
		while(x>=5);
	}

}
