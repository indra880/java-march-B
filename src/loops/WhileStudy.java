package loops;

public class WhileStudy {

	public static void main(String[] args) {
		
		int i=1;
		while(i<=10)
		{
			System.out.println(i);
			i=i+1;
		}
		System.out.println("===================================================================");
		
		int x=15;
		while(x<=150)
		{
			System.out.println(x);
			x=x+15;
		}
		System.out.println("====================================================================");
		
		int y=50;
		while(y>=3)
		{
			System.out.println(y);
			y=y-3;
		}
	}
	
	

}
