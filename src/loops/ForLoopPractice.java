package loops;

public class ForLoopPractice {

	public static void main(String[] args)
	{
		for(int i=7; i<=70; i=i+7)
		{
			System.out.println(i);
		}
		System.out.println("=============================================");
		
		
		for(int i=70; i>=7; i=i-7)
		{
			System.out.println(i);
		}
		System.out.println("=============================================");
		
		for(int i=1; i<=10; i=i+2)
		{
			System.out.println("INDRAJIT");
		}
		

	}

}
